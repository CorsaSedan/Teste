    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        btnAlterar.setEnabled(true);
        btnDesativar.setEnabled(true);
        
        Produto produto = (Produto) gtmProduto.getObjectList().get(jTable1.getSelectedRow());
        if (produto.getStatus().equals("desativado")) {
            btnDesativar.setText("Ativar");
        } else {
            btnDesativar.setText("Desativar");
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnDesativarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesativarActionPerformed
        // TODO add your handling code here:
        Produto produto = (Produto) gtmProduto.getObjectList().get(jTable1.getSelectedRow());
        if (produto.getStatus().equals("ativado")) {
            ProdutoBusiness.disable(produto);
        } else {
            ProdutoBusiness.activate(produto);
        }
        
        buscar();
        btnDesativar.setEnabled(false);
        btnAlterar.setEnabled(false);
    }//GEN-LAST:event_btnDesativarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        Produto produto = (Produto) gtmProduto.getObjectList().get(jTable1.getSelectedRow());
        new AlterarProduto(null, true, produto, this).setVisible(true);
        btnAlterar.setEnabled(false);
        btnDesativar.setEnabled(false);
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaActionPerformed
        // TODO add your handling code here:
        jTable1.clearSelection();
        btnAlterar.setEnabled(false);
        btnDesativar.setEnabled(false);
        
        String search = txtBusca.getText();
        String type;
        
        if (comboBox.getSelectedIndex() == 0) {
            type = "id";
        } else {
            type = "nome";
        }
        
        produtosFiltered = Produto.filter(produtos, type, search);
        atualizaTabela();
    }//GEN-LAST:event_btnBuscaActionPerformed

